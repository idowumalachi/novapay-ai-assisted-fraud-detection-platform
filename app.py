def prepare_features_for_model(df: pd.DataFrame, target: str = "is_fraud"):
    """
    Takes raw/feature CSV that still contains timestamp + categoricals,
    and returns X (numeric matrix) + y (labels).
    """
    df = df.copy()

    if target not in df.columns:
        raise ValueError(f"Target column '{target}' not found in training CSV.")

    y = df[target].astype(int).values
    X = df.drop(columns=[target])

    # ID columns should NOT be used as model features
    ID_COLS = ["transaction_id", "txn_id", "transactionId", "TransactionID"]
    X = X.drop(columns=[c for c in ID_COLS if c in X.columns], errors="ignore")


    # Handle timestamp -> time features
    if "timestamp" in X.columns:
        ts = pd.to_datetime(X["timestamp"], errors="coerce", utc=True)
        X["hour"] = ts.dt.hour.fillna(0).astype(int)
        X["dayofweek"] = ts.dt.dayofweek.fillna(0).astype(int)
        X["month"] = ts.dt.month.fillna(0).astype(int)
        X["is_weekend"] = (ts.dt.dayofweek >= 5).fillna(False).astype(int)
        X = X.drop(columns=["timestamp"])

    # One-hot encode categoricals + bools
    cat_cols = X.select_dtypes(include=["object", "bool"]).columns.tolist()
    if len(cat_cols) > 0:
        X = pd.get_dummies(X, columns=cat_cols, drop_first=False)

    # Clean numeric issues
    X = X.replace([np.inf, -np.inf], np.nan).fillna(0)

    return X, y


def align_features(X_any: pd.DataFrame, feature_list: list[str]) -> pd.DataFrame:
    """
    Ensures X has exactly the columns used in training, in the same order.
    Missing columns are created as 0, extra columns are dropped.
    """
    X = X_any.copy()
    for col in feature_list:
        if col not in X.columns:
            X[col] = 0
    X = X[feature_list]
    return X



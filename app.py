def risk_tier(prob: float) -> str:
    if prob >= 0.80:
        return "Critical"
    if prob >= 0.60:
        return "High"
    if prob >= 0.35:
        return "Medium"
    return "Low"


def score_df(df_in: pd.DataFrame, model, features: list[str], threshold: float):
    df = df_in.copy()
    ID_COLS = ["transaction_id", "txn_id", "transactionId", "TransactionID"]
    df = df.drop(columns=[c for c in ID_COLS if c in df.columns], errors="ignore")


    # If user uploads raw data with timestamp/categoricals, we must encode it
    # similarly to training (but WITHOUT target)
    if "timestamp" in df.columns:
        ts = pd.to_datetime(df["timestamp"], errors="coerce", utc=True)
        df["hour"] = ts.dt.hour.fillna(0).astype(int)
        df["dayofweek"] = ts.dt.dayofweek.fillna(0).astype(int)
        df["month"] = ts.dt.month.fillna(0).astype(int)
        df["is_weekend"] = (ts.dt.dayofweek >= 5).fillna(False).astype(int)
        df = df.drop(columns=["timestamp"])

    cat_cols = df.select_dtypes(include=["object", "bool"]).columns.tolist()
    if len(cat_cols) > 0:
        df = pd.get_dummies(df, columns=cat_cols, drop_first=False)

    df = df.replace([np.inf, -np.inf], np.nan).fillna(0)

    X = align_features(df, features)

    # RF supports predict_proba
    prob = model.predict_proba(X)[:, 1]
    out = df_in.copy()
    out["fraud_probability"] = prob
    out["fraud_flag"] = (out["fraud_probability"] >= threshold).astype(int)
    out["risk_tier"] = out["fraud_probability"].apply(risk_tier)

    return out


def generate_action_recommendation(row: pd.Series):
    reasons = []
    actions = []

    # These are simple rules to SOUND like an analyst assistant
    if row.get("fraud_probability", 0) >= 0.80:
        actions.append("Hold transaction and escalate to fraud queue immediately.")
        actions.append("Request step-up authentication (OTP/biometric) if allowed.")
    elif row.get("fraud_probability", 0) >= 0.60:
        actions.append("Flag for manual review before settlement.")
        actions.append("Monitor linked customer/device for rapid follow-up attempts.")
    else:
        actions.append("Allow transaction but keep monitoring risk trend.")

    # Common signals (only add if the column exists)
    if "ip_risk_score" in row and row["ip_risk_score"] >= 0.7:
        reasons.append("High IP risk score")
    if "txn_velocity_1h" in row and row["txn_velocity_1h"] >= 5:
        reasons.append("High 1-hour velocity")
    if "txn_velocity_24h" in row and row["txn_velocity_24h"] >= 15:
        reasons.append("High 24-hour velocity")
    if "account_age_days" in row and row["account_age_days"] <= 30:
        reasons.append("Very new account age")
    if "corridor_risk" in row and row["corridor_risk"] >= 0.6:
        reasons.append("High-risk corridor")

    if len(reasons) == 0:
        reasons.append("Overall feature pattern matched historical fraud behaviour in the model.")

    return reasons, actions


def metric_row(scored_df: pd.DataFrame):
    total = len(scored_df)
    flagged = int((scored_df["fraud_flag"] == 1).sum())
    crit = int((scored_df["risk_tier"] == "Critical").sum())
    high = int((scored_df["risk_tier"] == "High").sum())
    avg_prob = float(scored_df["fraud_probability"].mean()) if total else 0.0
    return total, flagged, crit, high, avg_prob


